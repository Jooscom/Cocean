package kr.co.cocean.statistics.dto;

public class TankHistoryDTO {
	private int branchID;
	private String branchName;
	private int totalNumber;
	private int normalNumber;
	private int abnormalNumber;
	private int seaWaterTankNumber;
	private int freshWaterTankNumber;
	private int semiAquaticTankNumber;
	private int landTankNumber;
	public int getBranchID() {
		return branchID;
	}
	public void setBranchID(int branchID) {
		this.branchID = branchID;
	}
	public String getBranchName() {
		return branchName;
	}
	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}
	public int getTotalNumber() {
		return totalNumber;
	}
	public void setTotalNumber(int totalNumber) {
		this.totalNumber = totalNumber;
	}
	public int getNormalNumber() {
		return normalNumber;
	}
	public void setNormalNumber(int normalNumber) {
		this.normalNumber = normalNumber;
	}
	public int getAbnormalNumber() {
		return abnormalNumber;
	}
	public void setAbnormalNumber(int abnormalNumber) {
		this.abnormalNumber = abnormalNumber;
	}
	public int getSeaWaterTankNumber() {
		return seaWaterTankNumber;
	}
	public void setSeaWaterTankNumber(int seaWaterTankNumber) {
		this.seaWaterTankNumber = seaWaterTankNumber;
	}
	public int getFreshWaterTankNumber() {
		return freshWaterTankNumber;
	}
	public void setFreshWaterTankNumber(int freshWaterTankNumber) {
		this.freshWaterTankNumber = freshWaterTankNumber;
	}
	public int getSemiAquaticTankNumber() {
		return semiAquaticTankNumber;
	}
	public void setSemiAquaticTankNumber(int semiAquaticTankNumber) {
		this.semiAquaticTankNumber = semiAquaticTankNumber;
	}
	public int getLandTankNumber() {
		return landTankNumber;
	}
	public void setLandTankNumber(int landTankNumber) {
		this.landTankNumber = landTankNumber;
	}
	
	
	
	
	
}
